chrome.commands.onCommand.addListener(function(command) {

  chrome.storage.local.get("definedURL", function(result) {
    if (result.definedURL != undefined) {
      chrome.tabs.update({
        url: 'http://' + result.definedURL
      })
    } else {
      chrome.tabs.update({
        url: 'http://i.imgur.com/Jl1wSJi.jpg'
      })
    }
  })

})